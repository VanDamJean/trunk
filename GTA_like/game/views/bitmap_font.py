def draw_text(surface, text, x, y, color=(255, 255, 255), scale=1):
    """
    텍스트를 비트맵으로 surface에 직접 그린다.
    scale=1 → 각 픽셀이 1×1, scale=2 → 2×2 등
    """
    cx = x
    for ch in text.upper():
        glyph = _GLYPHS.get(ch, _GLYPHS.get(' '))
        if glyph:
            for row_i, row in enumerate(glyph):
                for col_i, bit in enumerate(row):
                    if bit == '1':
                        px = cx + col_i * scale
                        py = y  + row_i * scale
                        if scale == 1:
                            surface.set_at((px, py), color)
                        else:
                            pygame.draw.rect(surface, color, (px, py, scale, scale))
        cx += (6) * scale  # 글자 폭 + 간격


def text_width(text, scale=1):
    return len(text) * 6 * scale
